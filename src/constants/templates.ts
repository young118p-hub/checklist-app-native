export function calculateQuantity(item: { baseQuantity?: number; multiplier?: number }, peopleCount: number = 1): number {
  if (item.multiplier === undefined || item.multiplier === null) return item.baseQuantity || 1;
  return (item.baseQuantity || 1) * (item.multiplier || 1) * Math.max(1, peopleCount);
}